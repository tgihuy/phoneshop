    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        OrderDBContext odb = new OrderDBContext();
        int oid = Integer.parseInt(request.getParameter("id"));
        Order order = odb.getOrderByID(oid);
        request.setAttribute("order", order);
        request.setAttribute("orderStatuses", odb.getAllOrderStatus());
        request.setAttribute("orderInfos", odb.getOrderInfoByOrderID(oid));
        request.getRequestDispatcher("views/OrderDetail.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        OrderDBContext odb = new OrderDBContext();
        
        int orderId = Integer.parseInt(request.getParameter("orderId"));
        int orderStatus = Integer.parseInt(request.getParameter("orderStatus"));
//        double total = Double.parseDouble(request.getParameter("total"));
//        String date = request.getParameter("date");
//        String address = request.getParameter("address");
//        String note = request.getParameter("note");
        
        odb.updateOrderStatus2(orderId, orderStatus);
        
        request.setAttribute("order", odb.get(orderId));
        request.setAttribute("orderStatuses", odb.getAllOrderStatus());
        response.sendRedirect("OrderList");
    }

